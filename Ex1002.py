# Ex1002) A fórmula para calcular a área de uma circunferência é: area = π . raio2. Considerando para este problema que π = 3.14159, efetue o cálculo da área, elevando o valor de raio ao quadrado e multiplicando por π.

#ENTRADA
raio = float(input(""))

#PROCESSAMENTO
Area = 3.14159 * (raio ** 2)

#SAIDA
print("A = %.4f" % Area)